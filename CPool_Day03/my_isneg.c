int     my_isneg(int n)
{
  int   nb;

  nb = 0;
  
  if ( n < nb )
    {
      my_putchar('N');
    }
  else if ( n > nb )
    {
      my_putchar('P');
    }
  else
    {
      my_putchar('P');
    }
}
